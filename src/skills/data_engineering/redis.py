async def invoke(payload: Dict[str, Any]) -> Dict[str, Any]:
    """
    Main entry point for skill invocation.
    
    Expected payload:
        - action: str (optional): Name of wrapper to call. If omitted, returns list of available wrappers
        - args: list (optional): Positional arguments for the wrapper
        - kwargs: dict (optional): Keyword arguments for the wrapper
    
    Returns:
        dict with 'result' and 'metadata' keys
    """
    start_time = time.time()
    timestamp = datetime.datetime.now().isoformat()
    
    action = payload.get("action")
    args = payload.get("args", [])
    kwargs = payload.get("kwargs", {})
    
    if action is None:
        return {
            "result": {
                "available_wrappers": list(WRAPPED_NAMES),
                "message": "Available wrappers. Use action parameter to call a specific wrapper."
            },
            "metadata": {
                "timestamp": timestamp,
                "library": "redis",
                "function_count": len(WRAPPED_NAMES)
            }
        }
    
    if action not in WRAPPED_NAMES:
        return {
            "result": {"error": "Unknown action: " + action},
            "metadata": {
                "timestamp": timestamp,
                "error": "Action not found in wrapped names"
            }
        }
    
    wrappers = {
        "AuthenticationError": AuthenticationError_wrapper,
        "AuthenticationWrongNumberOfArgsError": AuthenticationWrongNumberOfArgsError_wrapper,
        "BlockingConnectionPool": BlockingConnectionPool_wrapper,
        "BusyLoadingError": BusyLoadingError_wrapper,
        "ChildDeadlockedError": ChildDeadlockedError_wrapper,
        "Connection": Connection_wrapper,
        "ConnectionError": ConnectionError_wrapper,
        "ConnectionPool": ConnectionPool_wrapper,
        "CredentialProvider": CredentialProvider_wrapper,
        "CrossSlotTransactionError": CrossSlotTransactionError_wrapper,
        "DataError": DataError_wrapper,
        "InvalidPipelineStack": InvalidPipelineStack_wrapper,
        "InvalidResponse": InvalidResponse_wrapper,
        "MaxConnectionsError": MaxConnectionsError_wrapper,
        "OutOfMemoryError": OutOfMemoryError_wrapper,
        "PubSubError": PubSubError_wrapper,
        "ReadOnlyError": ReadOnlyError_wrapper,
        "Redis": Redis_wrapper,
        "RedisCluster": RedisCluster_wrapper,
        "RedisClusterException": RedisClusterException_wrapper,
        "RedisError": RedisError_wrapper,
        "ResponseError": ResponseError_wrapper,
        "SSLConnection": SSLConnection_wrapper,
        "Sentinel": Sentinel_wrapper,
        "SentinelConnectionPool": SentinelConnectionPool_wrapper,
        "SentinelManagedConnection": SentinelManagedConnection_wrapper,
        "SentinelManagedSSLConnection": SentinelManagedSSLConnection_wrapper,
        "StrictRedis": StrictRedis_wrapper,
        "TimeoutError": TimeoutError_wrapper,
        "UnixDomainSocketConnection": UnixDomainSocketConnection_wrapper,
        "UsernamePasswordCredentialProvider": UsernamePasswordCredentialProvider_wrapper,
        "WatchError": WatchError_wrapper,
        "default_backoff": default_backoff_wrapper,
        "from_url": from_url_wrapper,
        "int_or_str": int_or_str_wrapper,
    }
    
    try:
        result = await wrappers[action](args, kwargs)
        elapsed = time.time() - start_time
        
        return {
            "result": result,
            "metadata": {
                "timestamp": timestamp,
                "library": "redis",
                "library_version": "7.1.0" if "7.1.0" != "None" else None,
                "action": action,
                "elapsed_seconds": elapsed
            }
        }
    except Exception as e:
        logger.error("Error invoking {}: {}".format(action, str(e)))
        return {
            "result": {"error": str(e)},
            "metadata": {
                "timestamp": timestamp,
                "library": "redis",
                "error": str(e)
            }
        }


